/* 
--- React ES6 Spread Operator
-- Spread Operator
- The JS spread operator(...) allows us to quickly copy all or part of an existing array or object 
  into another array or object. 
- The spread operator is often used in combination with destructuring
- Example:
*/ 

const numberOne = [1, 2, 3]; 
const numbersTwo = [4, 5, 6]; 
const numbersCombined = [...numberOne, ...numbersTwo]; 
